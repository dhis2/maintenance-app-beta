export function generateLegendItems(
    startValue: number,
    endValue: number,
    colors: string[]
): LegendItem[] {
    const count = colors.length
    const step = (endValue - startValue) / count
    const precision = getPrecision(step)

    return colors.map((color, index) => {
        const itemStart = Number.parseFloat(
            (startValue + step * index).toFixed(precision)
        )
        const itemEnd = Number.parseFloat(
            (startValue + step * (index + 1)).toFixed(precision)
        )
        return {
            id: generateDhis2Id(),
            name: `${itemStart} - ${itemEnd}`,
            startValue: itemStart,
            endValue: itemEnd,
            color,
        }
    })
}

function getPrecision(step: number): number {
    if (step === 0) {
        return 0
    }
    const absStep = Math.abs(step)
    if (absStep >= 1) {
        return 0
    }
    return Math.ceil(-Math.log10(absStep)) + 1
}
